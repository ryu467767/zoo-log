def main():
    in_csv  = sys.argv[1] if len(sys.argv) > 1 else "zoos_list.csv"
    out_csv = sys.argv[2] if len(sys.argv) > 2 else "zoos_with_animals.csv"

    if not os.path.exists(in_csv):
        raise FileNotFoundError(f"{in_csv} が見つかりません")

    with open(in_csv, "r", encoding="utf-8-sig", newline="") as f:
        reader = csv.DictReader(f)
        fieldnames = list(reader.fieldnames)
        rows = list(reader)

    # フラグ列が無ければ追加
    animal_cols = list(ANIMAL_FLAGS.keys())
    for col in animal_cols:
        if col not in fieldnames:
            fieldnames.append(col)
    for row in rows:
        for col in animal_cols:
            row.setdefault(col, "False")

    updated = 0
    for row in rows:
        name = row["name"]
        changed = False
        for col, zoo_set in ANIMAL_FLAGS.items():
            new_val = "True" if name in zoo_set else "False"
            if row[col] != new_val:
                row[col] = new_val
                changed = True
        if changed:
            updated += 1

    with open(out_csv, "w", encoding="utf-8-sig", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)

    print(f"DONE  更新: {updated}/{len(rows)} 園")
    print(f"出力: {os.path.abspath(out_csv)}")

    # ---- サマリー表示 ----
    print("\n--- 動物フラグ集計 ---")
    for col in animal_cols:
        count = sum(1 for r in rows if r[col] == "True")
        print(f"  {col}: {count} 園")


if __name__ == "__main__":
    main()
